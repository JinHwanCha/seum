/**
 * 멤버의 월간 출석 점수(0..1)에 따라 적절한 성경 구절을 결정적으로 선택.
 * - 0.5 이상: 격려 구절
 * - 0.5 미만: 위로 구절
 * userId를 시드로 사용하여 같은 사람은 같은 구절을 받도록 함.
 */
export function pickVerseForMember(userId: string, score: number): BibleVerse {
  const pool = score >= 0.5 ? ENCOURAGEMENT_VERSES : COMFORT_VERSES;
  // 결정적 해시: userId 문자코드 합산
  let hash = 0;
  for (let i = 0; i < userId.length; i++) {
    hash = (hash * 31 + userId.charCodeAt(i)) >>> 0;
  }
  return pool[hash % pool.length];
}
